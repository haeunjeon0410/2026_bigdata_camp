export const RECIPES = csvRows.map((row, index) => {
  const fix = rowFixes[row.name] || {};
  const ingredients = row.ingredients.split(", ");
  const steps = row.steps
    .split(" | ")
    .map((step) => step.replace(/^\d+\.\s*/, ""));
  const cookingSequence =
    fix.cookingSequence ||
    row.cookingSequence
      .match(/["']([^"']+)["']/g)
      ?.map((item) => item.slice(1, -1)) ||
    [];
  return {
    id: `r${index + 1}`,
    name: row.name,
    category: row.category,
    emoji: row.emoji,
    difficulty: row.difficulty,
    time: `${row.cook_time}분`,
    need: ingredients
      .map(
        (name) =>
          INGREDIENTS.find((ingredient) => ingredient.name === name)?.id,
      )
      .filter(Boolean),
    missing: [],
    aiReason: fix.aiReason || row.aiReason,
    ingredients,
    steps,
    cookingSequence,
    substituteTips: substituteTipsMap[row.name] || [],
    cuisineType: cuisineTypeMap[row.name] || "",
  };
});

